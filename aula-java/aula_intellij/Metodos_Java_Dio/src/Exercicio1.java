

public class Exercicio1 {
    public static void soma(double parcela1, double parcela2) {
        double resultado = parcela1 + parcela2;
        System.out.println("A soma de " + parcela1 + " mais " + parcela2 + " é: " + resultado);
    }

    public static void subtracao(double minuendo, double subtraendo) {
        double resultado = minuendo - subtraendo;
        System.out.println("A subtração de " + minuendo + " menos " + subtraendo + " é: " + resultado);
    }

    public static void multiplicacao(double multiplicando, double multiplicador) {
        double resultado = multiplicando * multiplicador;
        System.out.println("A multiplicação de " + multiplicando + " vezes " + multiplicador + " é: " + resultado);
    }

    public static void divisao(double dividendo, double divisor) {
        double resultado = dividendo / divisor;
        System.out.println("A divisão de " + dividendo + " dividido por " + divisor + " é: " + resultado);
    }



}

